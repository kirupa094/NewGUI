    private void jsignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jsignupActionPerformed
        // TODO add your handling code here:
        if(utext2.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Please type User Name");
        }
        else if(ptext2.getText().length()==0)
        {
            JOptionPane.showMessageDialog(this, "Please type Password");
        }
        else if(ptext2.getText().equals(rptext2.getText()) == false)
        {
            JOptionPane.showMessageDialog(this, "Password is not match");
        }
        else
        try {
            String Username=utext2.getText();
            String ReTypePassword=rptext2.getText();

            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/UMS", "root", "");
            sts = con.prepareStatement("INSERT INTO course (Username,Password) VALUES (?, ?)");
            sts.setString(1, Username);
            sts.setString(2, ReTypePassword);

            sts.executeUpdate();
            JOptionPane.showMessageDialog(null,"New lecturer added.......");
            utext2.setText("");
            ptext2.setText("");
            rptext2.setText("");
            utext2.requestFocus();
            Lecturersignup m=new Lecturersignup(Username);

            m.setVisible(true);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jsignupActionPerformed

