    private void jbutton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbutton1ActionPerformed
        try {
            // TODO add your handling code here:

            if(utext1.getText().isEmpty() ||ptext1.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "UserName or Password Blank");
            }
            else
            {
                String Username=utext1.getText();
                String Password=ptext1.getText();

                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/UMS", "root", "");
                sts=con.prepareStatement("select * from course where Username =? and Password=?");
                sts.setString(1, Username);
                sts.setString(2, Password);
                rs=sts.executeQuery();
                if(rs.next())
                {
                   Lecturersignin m=new Lecturersignin(Username);
                    this.hide();
                    m.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "UserName or Password Error....");
                    utext1.setText("");
                    ptext1.setText("");
                    utext1.requestFocus();
                }
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbutton1ActionPerformed

